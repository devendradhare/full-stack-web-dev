# 5. What is the use of del keyword?

# ans->
#     In Python, the `del` keyword is used to delete objects or variables from memory. 
#     It can be used to remove elements from lists, delete variables, and even delete entire objects.

#     Here are a few common uses of the `del` keyword:

#     Deleting Variables:

#     x = 10
#     del x

#     This removes the variable `x` from memory, making it inaccessible 
#     and freeing up the memory previously occupied by `x`.